# -*- coding: utf-8 -*-
"""Frozen scorer for image generation prompt optimization.

Generates images via Gemini's imagen-3.0-generate-002 model, then scores them
on CLIP similarity, aesthetic quality, and cross-seed consistency.

Do not edit this file — the agent edits prompt.py only.
"""

import os
import sys
import time
import hashlib
from pathlib import Path
from io import BytesIO

import numpy as np
from PIL import Image

# ---------------------------------------------------------------------------
# Optional: real CLIP scoring via open_clip
# ---------------------------------------------------------------------------
_clip_available = False
try:
    import open_clip
    import torch

    _clip_model = None
    _clip_preprocess = None
    _clip_tokenizer = None

    def _load_clip():
        global _clip_model, _clip_preprocess, _clip_tokenizer, _clip_available
        if _clip_model is not None:
            return
        _clip_model, _, _clip_preprocess = open_clip.create_model_and_transforms(
            "ViT-B-32", pretrained="laion2b_s34b_b79k"
        )
        _clip_tokenizer = open_clip.get_tokenizer("ViT-B-32")
        _clip_model.eval()
        _clip_available = True

    _load_clip()
except ImportError:
    pass


def clip_score(image: Image.Image, prompt: str) -> float:
    """Return CLIP cosine similarity (0-100) between image and prompt."""
    if _clip_available:
        img_tensor = _clip_preprocess(image).unsqueeze(0)
        text_tokens = _clip_tokenizer([prompt])
        with torch.no_grad():
            img_features = _clip_model.encode_image(img_tensor)
            txt_features = _clip_model.encode_text(text_tokens)
            img_features /= img_features.norm(dim=-1, keepdim=True)
            txt_features /= txt_features.norm(dim=-1, keepdim=True)
            similarity = (img_features @ txt_features.T).item()
        # Map from ~[0.15, 0.40] cosine range to 0-100
        return max(0.0, min(100.0, (similarity - 0.15) / 0.25 * 100.0))

    # Fallback: deterministic simulated score based on image content + prompt
    img_arr = np.array(image.convert("RGB").resize((64, 64)))
    prompt_hash = int(hashlib.md5(prompt.encode()).hexdigest()[:8], 16)
    brightness = img_arr.mean() / 255.0
    color_std = img_arr.std() / 128.0
    base = 55.0 + 20.0 * brightness + 10.0 * color_std
    noise = ((prompt_hash % 1000) / 1000.0 - 0.5) * 10.0
    return max(0.0, min(100.0, base + noise))


def aesthetic_score(image: Image.Image) -> float:
    """Heuristic aesthetic score (0-100) based on sharpness, color, contrast."""
    img_arr = np.array(image.convert("RGB"), dtype=np.float64)
    gray = np.array(image.convert("L"), dtype=np.float64)

    # Sharpness via Laplacian variance
    # Simple 3x3 Laplacian kernel convolution
    laplacian = (
        gray[:-2, 1:-1] + gray[2:, 1:-1] + gray[1:-1, :-2] + gray[1:-1, 2:]
        - 4 * gray[1:-1, 1:-1]
    )
    sharpness = np.var(laplacian)
    sharpness_score = min(100.0, sharpness / 20.0)  # normalize

    # Color richness: std across channels
    color_richness = img_arr.std(axis=(0, 1)).mean()
    color_score = min(100.0, color_richness / 0.7)

    # Contrast: range of intensity
    p5, p95 = np.percentile(gray, [5, 95])
    contrast = (p95 - p5) / 255.0
    contrast_score = contrast * 100.0

    # Weighted combination
    return 0.4 * sharpness_score + 0.3 * color_score + 0.3 * contrast_score


def consistency_score(images: list[Image.Image]) -> float:
    """Score (0-100) for visual consistency across images. Higher = more consistent."""
    if len(images) < 2:
        return 100.0

    # Compare histograms across images
    histograms = []
    for img in images:
        arr = np.array(img.convert("RGB").resize((128, 128)), dtype=np.float64)
        # Normalized color histogram (coarse: 8 bins per channel)
        hist = np.histogramdd(
            arr.reshape(-1, 3), bins=8, range=((0, 256), (0, 256), (0, 256))
        )[0]
        hist = hist / hist.sum()
        histograms.append(hist.flatten())

    # Mean pairwise similarity (1 - L1 distance / 2)
    similarities = []
    for i in range(len(histograms)):
        for j in range(i + 1, len(histograms)):
            l1 = np.abs(histograms[i] - histograms[j]).sum()
            similarities.append(1.0 - l1 / 2.0)

    return np.mean(similarities) * 100.0


def generate_images(prompt_config) -> list[tuple[Image.Image, int]]:
    """Generate images using Gemini gemini-3.1-flash-image-preview. Returns list of (image, seed)."""
    from google import genai
    from google.genai import types

    api_key = os.environ.get("GOOGLE_API_KEY")
    if not api_key:
        print("ERROR: GOOGLE_API_KEY environment variable not set", file=sys.stderr)
        sys.exit(1)

    client = genai.Client(api_key=api_key)

    # Build the full prompt
    parts = [prompt_config.POSITIVE_PROMPT]
    if prompt_config.STYLE_TOKENS:
        parts.append(prompt_config.STYLE_TOKENS)
    if prompt_config.NEGATIVE_PROMPT:
        parts.append(f"Avoid: {prompt_config.NEGATIVE_PROMPT}")
    full_prompt = ", ".join(parts)

    seeds = prompt_config.SEEDS[: prompt_config.NUM_SEEDS]
    results = []

    for seed in seeds:
        try:
            response = client.models.generate_content(
                model="gemini-3.1-flash-image-preview",
                contents=f"Generate an image: {full_prompt}. Use seed {seed} for consistency.",
                config=types.GenerateContentConfig(
                    response_modalities=["IMAGE", "TEXT"],
                ),
            )
            # Extract image from response parts
            if response.candidates and response.candidates[0].content.parts:
                for part in response.candidates[0].content.parts:
                    if part.inline_data and part.inline_data.mime_type.startswith("image/"):
                        img_bytes = part.inline_data.data
                        img = Image.open(BytesIO(img_bytes))
                        results.append((img, seed))
                        break
                else:
                    print(f"  WARNING: No image in response for seed {seed}", file=sys.stderr)
            else:
                print(f"  WARNING: Empty response for seed {seed}", file=sys.stderr)
        except Exception as e:
            print(f"  ERROR generating image with seed {seed}: {e}", file=sys.stderr)

    return results


def main():
    t0 = time.time()

    # Import mutable prompt config
    sys.path.insert(0, str(Path(__file__).parent))
    import prompt as prompt_config

    # Generate images
    print("Generating images...", file=sys.stderr)
    results = generate_images(prompt_config)

    if not results:
        print("ERROR: No images were generated", file=sys.stderr)
        sys.exit(1)

    # Save images
    outputs_dir = Path(__file__).parent / "outputs"
    outputs_dir.mkdir(exist_ok=True)
    images = []
    for img, seed in results:
        path = outputs_dir / f"seed_{seed}.png"
        img.save(path)
        images.append(img)
        print(f"  Saved {path}", file=sys.stderr)

    # Score
    full_prompt = prompt_config.POSITIVE_PROMPT
    if prompt_config.STYLE_TOKENS:
        full_prompt += ", " + prompt_config.STYLE_TOKENS

    clip_scores_list = [clip_score(img, full_prompt) for img in images]
    aesthetic_scores_list = [aesthetic_score(img) for img in images]
    avg_clip = np.mean(clip_scores_list)
    avg_aesthetic = np.mean(aesthetic_scores_list)
    consist = consistency_score(images)

    composite = 0.4 * avg_clip + 0.3 * avg_aesthetic + 0.3 * consist

    elapsed = time.time() - t0

    print("---")
    print(f"score:            {composite:.1f}")
    print(f"clip_score:       {avg_clip:.1f}")
    print(f"aesthetic_score:  {avg_aesthetic:.1f}")
    print(f"consistency:      {consist:.1f}")
    print(f"images_generated: {len(images)}")
    print(f"eval_seconds:     {elapsed:.1f}")


if __name__ == "__main__":
    main()
