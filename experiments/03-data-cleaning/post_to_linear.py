def main():
    # Get environment variables
    api_key = os.getenv("LINEAR_API_KEY")
    if not api_key:
        print("Error: LINEAR_API_KEY environment variable not set")
        return 1

    # Issue ID from webhook
    issue_id = "86470c49-41d4-48a8-bccd-c006451fca3a"
    session_id = "6676d8c9"
    pr_number = 1360

    print(f"Posting results to Linear issue {issue_id}...")

    # Post summary comment
    comment_body = format_summary_comment(session_id, pr_number)
    try:
        comment = post_comment_to_issue(issue_id, comment_body, api_key)
        print(f"✓ Posted summary comment (ID: {comment['id']})")
    except Exception as e:
        print(f"✗ Failed to post summary comment: {e}")
        return 1

    print("\n✅ Comment posted successfully!")
    return 0


if __name__ == "__main__":
    sys.exit(main())
